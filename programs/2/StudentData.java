/* To store a student age, GPA, first and last initial and full attendance 
status */

public class StudentData {
    public static void main(String[] args) {
        int studentAge = 20;
        double studentGPA = 8.15;
        char firstInitial = 'J';
        char lastInitial = 'S';
        boolean fullAttendance = false;

        System.out.println(studentAge);
        System.out.println(studentGPA);
        System.out.println(firstInitial);
        System.out.println(lastInitial);
        System.out.println(fullAttendance);
    }
}